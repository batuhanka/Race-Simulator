    struct AISettingsView: View {
        let raceDays: [BetRaceDay]
        @Binding var selectedDayIndex: Int
        @Binding var selectedBetIndex: Int
        @Binding var budget: String
        @FocusState.Binding var isBudgetFocused: Bool
        
        let themePrimary: Color
        let themeAccent: Color
        let quickBudgets: [String]
        
        let onGenerate: () -> Void
        let onBack: () -> Void
        
        let getGanyanTypes: (BetRaceDay?) -> [BetType]
        let betTypeLabelForAI: (BetType, [BetType]) -> String
        let formattedRaceDayTitle: (BetRaceDay?) -> String

        var body: some View {
            VStack(spacing: 16) {
                VStack(spacing: 12) {
                    settingRow(title: "HİPODROM", icon: "mappin.and.ellipse") {
                        Menu {
                            Picker("Hipodrom", selection: $selectedDayIndex) {
                                ForEach(0..<raceDays.count, id: \.self) { index in
                                    Text(formattedRaceDayTitle(raceDays[index])).tag(index)
                                }
                            }
                        } label: {
                            HStack {
                                let currentDay = selectedDayIndex < raceDays.count ? raceDays[selectedDayIndex] : nil
                                Text(formattedRaceDayTitle(currentDay)).font(.subheadline.bold())
                                Spacer(); Image(systemName: "chevron.up.chevron.down").font(.caption2)
                            }
                            .foregroundColor(.white).padding(.vertical, 10).padding(.horizontal, 14).background(Color.black.opacity(0.3)).cornerRadius(10)
                        }
                    }
                    
                    settingRow(title: "BAHİS", icon: "list.bullet.rectangle.fill") {
                        let currentDay = selectedDayIndex < raceDays.count ? raceDays[selectedDayIndex] : nil
                        let available = getGanyanTypes(currentDay)
                        
                        Menu {
                            Picker("Bahis Türü", selection: $selectedBetIndex) {
                                ForEach(0..<available.count, id: \.self) { index in
                                    Text(betTypeLabelForAI(available[index], available)).tag(index)
                                }
                            }
                        } label: {
                            HStack {
                                let currentBet = selectedBetIndex < available.count ? available[selectedBetIndex] : nil
                                Text(currentBet != nil ? betTypeLabelForAI(currentBet!, available) : "Seçiniz").font(.subheadline.bold())
                                Spacer(); Image(systemName: "chevron.up.chevron.down").font(.caption2)
                            }
                            .foregroundColor(.white).padding(.vertical, 10).padding(.horizontal, 14).background(Color.black.opacity(0.3)).cornerRadius(10)
                        }
                    }
                    
                    settingRow(title: "BÜTÇE", icon: "turkishlirasign.circle.fill") {
                        VStack(alignment: .leading, spacing: 8) {
                            HStack {
                                Image(systemName: "turkishlirasign").font(.system(size: 14)).foregroundColor(themeAccent)
                                TextField("", text: $budget)
                                    .keyboardType(.decimalPad)
                                    .font(.system(size: 18, weight: .bold))
                                    .foregroundColor(themeAccent)
                                    .focused($isBudgetFocused)
                                Text("₺").font(.caption).foregroundColor(.secondary)
                            }
                            .padding(.vertical, 10).padding(.horizontal, 14).background(Color.black.opacity(0.3)).cornerRadius(10)
                            
                            ScrollView(.horizontal, showsIndicators: false) {
                                HStack(spacing: 6) {
                                    ForEach(quickBudgets, id: \.self) { amount in
                                        Button(action: {
                                            budget = amount
                                            isBudgetFocused = false
                                        }) {
                                            Text("\(amount)₺")
                                                .font(.system(size: 11, weight: .bold))
                                                .padding(.horizontal, 12)
                                                .padding(.vertical, 6)
                                                .background(budget == amount ? themePrimary.opacity(0.2) : Color.white.opacity(0.05))
                                                .foregroundColor(budget == amount ? themePrimary : .white)
                                                .cornerRadius(16)
                                                .overlay(RoundedRectangle(cornerRadius: 16).stroke(budget == amount ? themePrimary : Color.white.opacity(0.1), lineWidth: 1))
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
                .padding(16).background(Color.white.opacity(0.05)).cornerRadius(20)
                .overlay(RoundedRectangle(cornerRadius: 20).stroke(Color.white.opacity(0.1), lineWidth: 1))
                
                Button(action: onGenerate) {
                    Text("HAZIRLA").font(.headline.bold()).foregroundColor(.black).frame(maxWidth: .infinity).padding(.vertical, 16).background(themePrimary).cornerRadius(16).shadow(color: themePrimary.opacity(0.4), radius: 10, x: 0, y: 4)
                }
                
