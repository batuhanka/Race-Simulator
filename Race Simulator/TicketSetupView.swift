                HStack(spacing: 5) {
                    Image(systemName: icon).font(.system(size: 9))
                    Text(title).font(.system(size: 10, weight: .black))
                }
                .foregroundColor(themePrimary).padding(.leading, 2)
                content()
            }
        }
    }
    
    struct ProcessingOverlay: View {
